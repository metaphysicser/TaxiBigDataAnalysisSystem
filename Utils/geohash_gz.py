from pygeohash import encode


# sampling points in each administrative district
baiyun = [[113.273238, 23.157032], [113.272567, 23.157022]]
conghua = [[113.586460, 23.548350], [113.585543, 23.548375]]
haizhu = [[113.317200, 23.083310], [113.317849, 23.083325]]
huadu = [[113.220260, 23.403910], [113.219235, 23.404284]]
huangpu = [[113.480670, 23.181390], [113.479914, 23.181765]]
liwan = [[113.244280, 23.125860], [113.244087, 23.125761]]
nansha = [[113.524970, 22.801690], [113.525083, 22.802541]]
panyu = [[113.384240, 22.937720], [113.384675, 22.937848]]
tianhe = [[113.361990, 23.124630], [113.361502, 23.125326]]
yuexiu = [[113.266800, 23.129010], [113.266843, 23.128551]]
zengcheng = [[113.810900, 23.260930], [113.811243, 23.260353]]

areas = [baiyun, conghua, haizhu, huadu, huangpu, liwan, nansha, panyu, tianhe, yuexiu, zengcheng]
area_names = ['baiyun', 'conghua', 'haizhu', 'huadu', 'huangpu', 'liwan', 'nansha', 'panyu', 'tianhe', 'yuexiu', 'zengcheng']

for name in area_names:
    for i in areas:
        geohash_result = []
        for j in i:
            geohash_result.append(encode(j[1], j[0]))
        save_path = './data/geohash_gz/{}_geohash.txt'.format(name)
        file = open(save_path, 'w')
        file.write(','.join(geohash_result))
        file.close()


